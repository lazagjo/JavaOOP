package com.example;

public class Student {
    private String name;
    private int age;
    private int id_No;

    public Student() {
        this.name = name;
        this.age = age;
        this.id_No = id_No;
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getId_No() {
        return id_No;
    }

    public void setId_No(int id_No) {
        this.id_No = id_No;
    }
}
